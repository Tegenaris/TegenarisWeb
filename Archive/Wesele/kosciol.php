			<p>
				Zwiększająca się liczba mieszkańców Lubonia i Żabikowa, wywołała konieczność powstania nowej parafii.
				W 1931 roku zawiązał się tymczasowy komitet budowy kościoła.
				Od 1933 roku proboszczem parafii żabikowskiej był ks. Stanisław Streich, który czynnie włączył się w działalność komitetu.
				Jesienią 1934 roku ksiądz Streich nabył od Uniwersytetu Poznańskiego grunt pod budowę przyszłej świątyni.
				Architekt Sawicki przygotował projekt budowli, a pracami kierował Franciszek Reszelski.
				6 października 1935 roku ks. Stanisław Streich odprawił pierwszą mszę przy bocznej nawie powstającego kościoła pod wezwaniem św. Jana Bosko.
				W kolejnych latach rozbudowano i wykończono wnętrze kościoła, a w 1939 roku powstała dzwonnica.
				Przez lata okupacji niemieckiej w murach kościoła magazynowano materiały sanitarne.
				Po wyzwoleniu, latem 1945 roku rozpoczęto odbudowę spalonej przez hitlerowców i zniszczonej działaniami wojennymi świątyni.
				W październiku 1946 roku odprawiono w niej, pierwsze po zakończeniu wojny, nabożeństwo.
				W latach 1976–1981 przeprowadzono przebudowę wnętrza kościoła.
				Od 2002 roku budynek świątyni prezentuje się w nowej, odmłodzonej szacie, którą zyskał przez otynkowanie ceglanych murów.
